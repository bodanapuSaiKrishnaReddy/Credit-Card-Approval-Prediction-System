@app.route('/reports')
def reports():
    inquiries_list = load_inquiries()
    total = len(inquiries_list)
    approved_count = sum(1 for x in inquiries_list if x['decision'] == 'Approved')
    approval_rate = (approved_count / total * 100) if total > 0 else 0.0
    
    # Calculate income tier distributions
    tiers = {
        "under_50": {"total": 0, "approved": 0},
        "50_100": {"total": 0, "approved": 0},
        "100_150": {"total": 0, "approved": 0},
        "over_150": {"total": 0, "approved": 0}
    }
    for x in inquiries_list:
        inc = x['income']
        is_app = x['decision'] == 'Approved'
        if inc < 50000:
            key = "under_50"
        elif inc < 100000:
            key = "50_100"
        elif inc < 150000:
            key = "100_150"
        else:
            key = "over_150"
            
        tiers[key]["total"] += 1
        if is_app:
            tiers[key]["approved"] += 1
            
    rates = {}
    for k, v in tiers.items():
        rates[k] = (v["approved"] / v["total"] * 100) if v["total"] > 0 else 0.0
        
    return render_template('reports.html', total=total, approval_rate=approval_rate, rates=rates)

