@app.route('/review', methods=['POST'])
def review():
    if request.method == 'POST':
        try:
            validated_data = CreditForm(**request.form.to_dict())
        except ValidationError as e:
            errors = []
            for error in e.errors():
                field = error['loc'][0] if error['loc'] else 'unknown'
                msg = error['msg']
                errors.append({"field": field, "message": msg})
            return jsonify({"error": "validation_error", "details": errors}), 400
        
        return render_template('review.html', data=validated_data)

@app.route('/predict', methods=['POST'])
def predict():
    if request.method == 'POST':
        # Wrap all request.form casts in a Pydantic validation layer (Task 4)
        try:
            validated_data = CreditForm(**request.form.to_dict())
        except ValidationError as e:
            errors = []
            for error in e.errors():
                field = error['loc'][0] if error['loc'] else 'unknown'
                msg = error['msg']
                errors.append({"field": field, "message": msg})
            return jsonify({"error": "validation_error", "details": errors}), 400
        
        raw_data = {
            'CODE_GENDER': validated_data.gender,
            'FLAG_OWN_CAR': validated_data.car,
            'FLAG_OWN_REALTY': validated_data.realty,
            'CNT_CHILDREN': validated_data.children,
            'AMT_INCOME_TOTAL': validated_data.income,
            'NAME_INCOME_TYPE': validated_data.income_type,
            'NAME_EDUCATION_TYPE': validated_data.education,
            'NAME_FAMILY_STATUS': validated_data.family_status,
            'NAME_HOUSING_TYPE': validated_data.housing_type,
            'DAYS_BIRTH': -validated_data.age * 365,       # Dataset keeps days relative to today (negative)
            'DAYS_EMPLOYED': -int(validated_data.experience * 365),
            'FLAG_MOBIL': 1,
            'FLAG_WORK_PHONE': 0,                          # Defaulted as not collected in form
            'FLAG_PHONE': 1 if validated_data.phone.strip() else 0,
            'FLAG_EMAIL': 1 if validated_data.email.strip() else 0,
            'OCCUPATION_TYPE': validated_data.occupation,
            'CNT_FAM_MEMBERS': validated_data.family_size
        }
        
        input_df = pd.DataFrame([raw_data])
        
        # Transform categorical elements using saved configurations
        for col in encoders.keys():
            if col in input_df.columns:
                val = input_df.loc[0, col]
                # Reject the request with a clear 400 error listing unrecognized category values (Task 3)
                if val not in encoders[col].classes_:
                    return jsonify({
                        "error": "validation_error",
                        "field": col,
                        "message": f"Unrecognized category value '{val}' for field '{col}'"
                    }), 400
                input_df[col] = encoders[col].transform(input_df[col])

        # Run normalization transformations
        scaled_input = scaler.transform(input_df)
        
        # Run calculation using classification threshold constant (Task 7)
        # Class 1 corresponds to "Bad" (rejection), Class 0 is "Good" (approval)
        probabilities = model.predict_proba(scaled_input)[0]
        prediction = 1 if probabilities[1] >= DECISION_THRESHOLD else 0
        
        # Save inquiry record dynamically
        inquiry_record = {
            "full_name": validated_data.full_name,
            "income": validated_data.income,
            "age": validated_data.age,
            "income_type": validated_data.income_type,
            "decision": "Approved" if prediction == 0 else "Declined",
            "date": datetime.now().strftime("%B %d, %Y")
        }
        save_inquiry(inquiry_record)
        
        # Render the predictor result screen (Screen 4)
        return render_template('result.html', prediction=prediction, data=validated_data)

