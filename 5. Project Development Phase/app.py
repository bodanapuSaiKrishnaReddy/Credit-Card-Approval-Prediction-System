from flask import Flask, request, render_template, jsonify
import pickle
import numpy as np
import pandas as pd
from pydantic import BaseModel, ValidationError, Field
import os
import json
from datetime import datetime

DATA_FILE = 'inquiries.json'

def load_inquiries():
    if not os.path.exists(DATA_FILE):
        return []
    try:
        with open(DATA_FILE, 'r') as f:
            return json.load(f)
    except:
        return []

def save_inquiry(inquiry):
    inquiries_list = load_inquiries()
    inquiries_list.append(inquiry)
    try:
        with open(DATA_FILE, 'w') as f:
            json.dump(inquiries_list, f, indent=4)
    except Exception as e:
        print(f"Error saving inquiry: {e}")

app = Flask(__name__)

# Decision threshold constant for classification (Task 7)
# Higher values (>0.5) decrease False Approvals (reduces credit default risk) but increase False Rejections.
# Lower values (<0.5) increase False Approvals (higher credit default risk) but decrease False Rejections.
DECISION_THRESHOLD = 0.5

# SECURITY WARNING: Loading untrusted pickle files is a security risk (arbitrary code execution).
# These .pkl files must only ever be generated by this repository's own training pipeline (pipeline.py).
# Never load pickle files supplied by untrusted users or external sources.
# Load trained components
with open('model.pkl', 'rb') as m_file:
    model = pickle.load(m_file)
with open('scaler.pkl', 'rb') as s_file:
    scaler = pickle.load(s_file)
with open('encoders.pkl', 'rb') as e_file:
    encoders = pickle.load(e_file)

# Input validation schema matching the HTML form naming (Screen 2)
class CreditForm(BaseModel):
    # WARNING: Using gender as an input feature is legally restricted for real
    # credit decisions under fair-lending laws (e.g., ECOA / Reg B in the US).
    # TODO: Ask the user whether to drop this feature before deploying to production.
    full_name: str = Field(alias="full-name")
    gender: str
    car: str
    realty: str
    children: int
    income: float
    income_type: str = Field(alias="income-type")
    education: str
    family_status: str = Field(alias="family-status")
    housing_type: str = Field(alias="housing-type")
    age: int
    experience: float = Field(alias="work-exp")
    phone: str
    email: str
    occupation: str
    family_size: int = Field(alias="family-size")

